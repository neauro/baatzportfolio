  <h2>Biography</h2>
<p>Fong moved from Taiwan to Seattle in 1987, and graduated from the University of Washington Painting Program in 2000. She has been featured in several shows and has received various awards during her art career. In addition to her time spent painting, she has taught after school art programs and helped run community program workshops. Her pieces are mainly portraiture and figurative with an impressionistic emphasis. She finds the artistic rendition of people to be a satisfyingly complex challenge.  </p>

